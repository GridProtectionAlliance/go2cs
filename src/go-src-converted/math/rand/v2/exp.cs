        var u = r.Uint64();
        var j = (uint32)u;
        var i = (uint8)((u >> (int)(32)));
        var x = (float64)j * (float64)we[i];
        if (j < ke[i]) {
            return x;
        }
        if (i == 0) {
            return (float64)re - math.Log(r.Float64());
        }
        if (fe[i] + (float32)r.Float64() * (fe[i - 1] - fe[i]) < (float32)math.Exp(-x)) {
            return x;
        }
    }
