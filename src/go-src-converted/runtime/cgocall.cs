            throw panic(((errorString)msg));
        }
        cgoCheckArg(it, Δp, (abiꓸKind)((~it).Kind_ & abi.KindDirectIface) == 0, false, msg);
    }
    else if (exprᴛ1 == abi.Slice) {
        var st = (ж<slicetype>)(uintptr)(new @unsafe.Pointer(Ꮡt));
        var s = (ж<Δsliceᴛ>)(uintptr)(Δp);
