            // always be allocated in the Go heap. It's never OK
            // to pass them to C.
            Δp.Value = ~(ж<@unsafe.Pointer>)(uintptr)(Δp);
        }
        if (!cgoIsGoPointer(Δp)) {
            return;
        }
        throw panic(((errorString)msg));
    }
    else if (exprᴛ1 == abi.Interface) {
        var it = ~(ж<ж<_type>>)(uintptr)(Δp);
        if (it == nil) {
            return;
        }
