using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.IO;
using System.Linq;
using System.Reflection;
using System.Text.RegularExpressions;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.CSharp;
using static go2cs.Symbols;

namespace go2cs;

public static class Common
{
    public static readonly Assembly? EntryAssembly;
    public static readonly string AssemblyName;
    public static readonly string Version;
    public static readonly string GeneratedCodeAttribute;
    public static readonly Regex PointerExpr;
    private static readonly HashSet<char> InvalidChars;

    public const string AutoGeneratedHeader = 
        """
        //---------------------------------------------------------
        // <auto-generated>
        //     This code was generated by a tool. Changes to this
        //     file may cause incorrect behavior and will be lost
        //     if the code is regenerated.
        // </auto-generated>
        //---------------------------------------------------------
        """;

    static Common()
    {
        const string FallBackAssemblyName = nameof(go2cs);
        const string FallBackVersion = "0.0.1";

        try
        {
            EntryAssembly = Assembly.GetAssembly(typeof(Common));
            AssemblyName = EntryAssembly?.GetName().Name ?? FallBackAssemblyName;
            Version = getVersion();
        }
        catch
        {
            EntryAssembly = null;
            AssemblyName = FallBackAssemblyName;
            Version = FallBackVersion;
        }

        GeneratedCodeAttribute = $"""GeneratedCode("{AssemblyName}", "{Version}")""";

        PointerExpr = new Regex($"^(?:[^<]*)?{PointerPrefix}<.*$", RegexOptions.Compiled);
        InvalidChars = [..Path.GetInvalidFileNameChars()];

        return;

        string getVersion()
        {
            Version? version = EntryAssembly?.GetName().Version;
            return version is null ? FallBackVersion : $"{version.Major}.{version.Minor}.{version.Build}";
        }
    }

    public static bool IsGenericType(string typeName)
    {
        return typeName.Contains('<') && typeName.Contains('>');
    }

    public static string GetSimpleName(string typeName, bool dropGeneric = false, bool dropCollisionPrefix = false)
    {
        // Check if type name is a pointer, i.e., ж<T>
        int startIndex = typeName.IndexOf(PointerPrefix, StringComparison.Ordinal);
        bool isPointer = false;

        // For pointer types, get dereferenced underlying type
        if (startIndex > -1 && typeName.EndsWith(">"))
        {
            typeName = $"{typeName[(startIndex + 2)..^1]}";
            isPointer = true;
        }

        string[] parts = typeName.Split('.');

        string simpleName = $"{parts[^1]}{(isPointer ? ".val" : "")}";

        if (dropGeneric)
        {
            startIndex = typeName.IndexOf('<');

            if (startIndex > 1 && typeName.EndsWith(">"))
                simpleName = $"{simpleName[..startIndex]}";
        }

        if (dropCollisionPrefix && simpleName.StartsWith("Δ"))
            simpleName = simpleName[1..];

        return simpleName;
    }

    public static string GetFullTypeName(this ITypeSymbol? typeSymbol, bool useDisplayString = false)
    {
        switch (typeSymbol)
        {
            case null:
                return string.Empty;
            case IArrayTypeSymbol arrayType:
                return $"{GetFullTypeName(arrayType.ElementType)}[]";
            case INamedTypeSymbol { IsGenericType: true } namedType:
                ImmutableArray<ITypeSymbol> typeArgs = namedType.TypeArguments;
                IEnumerable<string> typeArgNames = typeArgs.Select(typeArg => typeArg.GetFullTypeName(useDisplayString));
                return $"{namedType.Name}<{string.Join(", ", typeArgNames)}>";
            default:
                return useDisplayString ? typeSymbol.ToDisplayString() : typeSymbol.Name;
        }
    }

    public static string[] GetFullyQualifiedUsingStatements(SyntaxTree syntaxTree, SemanticModel semanticModel)
    {
        return syntaxTree
            .GetRoot()
            .DescendantNodes()
            .OfType<UsingDirectiveSyntax>()
            // Skip `global using` directives: they are already in scope in every file of the
            // compilation (including generated ones), so re-emitting them as file-local `using`
            // here would collide with the original global alias (CS1537 "appeared previously").
            .Where(directive => directive.GlobalKeyword.IsKind(SyntaxKind.None))
            .Select(directive =>
            {
                if (directive.Name is null)
                    return "";

                // For alias directives, maintain the alias but fully qualify the target
                if (directive.Alias is not null)
                {
                    ISymbol? symbol = semanticModel.GetSymbolInfo(directive.Name).Symbol;

                    if (symbol is not null)
                    {
                        string fullyQualifiedName = symbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
                        return $"using {directive.Alias.Name.Identifier.Text} = {fullyQualifiedName};";
                    }
                    
                    return directive.GetText().ToString().Trim();
                }
                
                // Static using directives
                if (directive.StaticKeyword.Kind() is not SyntaxKind.None)
                {
                    ISymbol? symbol = semanticModel.GetSymbolInfo(directive.Name).Symbol;

                    if (symbol is not null)
                    {
                        string fullyQualifiedName = symbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
                        return $"using static {fullyQualifiedName};";
                    }

                    return directive.GetText().ToString().Trim();
                }

                // Standard namespace imports
                ISymbol? namespaceSymbol = semanticModel.GetSymbolInfo(directive.Name).Symbol;

                if (namespaceSymbol is not null)
                {
                    return $"using {namespaceSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)};";
                }
                
                return directive.GetText().ToString().Trim();
            })
            .ToArray();
    }

    public static string GetValidFileName(string fileName)
    {
        return new string(fileName.Replace("@", "").Select(c => InvalidChars.Contains(c) ? '_' : c).ToArray());
    }

    // Roslyn requires every AddSource hintName within a generator to be unique, and it compares them
    // CASE-INSENSITIVELY. Two Go types that differ only in case (e.g. exported `Pinner` and unexported
    // `pinner`, both real, distinct C# types) therefore collide on `…pinner.g.cs` and the generator
    // throws — suppressing ALL of its output. Disambiguate the later collisions with a numeric suffix;
    // the hintName is only an internal file label, so any unique value is correct.
    public static string GetUniqueHintName(HashSet<string> emitted, string hintName)
    {
        if (emitted.Add(hintName))
            return hintName;

        string baseName = hintName.EndsWith(".g.cs") ? hintName[..^5] : hintName;
        string suffix = hintName.EndsWith(".g.cs") ? ".g.cs" : "";

        int index = 1;
        string candidate;

        do
        {
            candidate = $"{baseName}.{index++}{suffix}";
        }
        while (!emitted.Add(candidate));

        return candidate;
    }

    public static string GetUnsanitizedIdentifier(string identifier)
    {
        return identifier.StartsWith("@") ? identifier[1..] : identifier;
    }

    public static string GetScope(string identifier)
    {
        char firstChar = identifier[0];

        // A '_'-prefixed Go identifier (other than the bare blank '_') is unexported → internal,
        // matching the converter's getAccess. Treating any '_' lead as public made a generated
        // promoted accessor for an embedded `*_func` public while the converter's `internal partial
        // ref` declaration was internal (CS8799).
        if (firstChar == '_')
            return identifier.Length == 1 ? "public" : "internal";

        return char.IsUpper(firstChar) ? "public" : "internal";
    }

    // Returns the explicit C# access modifier on a type declaration (e.g. the converter-emitted
    // `public partial struct d`), or null when none is present. This lets an explicit modifier
    // from the converter override the name-based GetScope — e.g. an unexported type that the
    // converter publicized because it is used as the type of an exported field (CS0051/CS0052).
    public static string GetExplicitAccessModifier(BaseTypeDeclarationSyntax typeDeclaration)
    {
        foreach (SyntaxToken modifier in typeDeclaration.Modifiers)
        {
            switch (modifier.Kind())
            {
                case SyntaxKind.PublicKeyword:
                    return "public";
                case SyntaxKind.InternalKeyword:
                    return "internal";
                case SyntaxKind.PrivateKeyword:
                    return "private";
                case SyntaxKind.ProtectedKeyword:
                    return "protected";
            }
        }

        return null;
    }
}
